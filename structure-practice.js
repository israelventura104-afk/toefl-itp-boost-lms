const quizState = { index: 0, answers: new Map() };

const questionEl = document.querySelector("[data-question]");
const metaEl = document.querySelector("[data-question-meta]");
const optionsEl = document.querySelector("[data-options]");
const feedbackEl = document.querySelector("[data-feedback]");
const counterEl = document.querySelector("[data-counter]");
const prevBtn = document.querySelector("[data-prev]");
const nextBtn = document.querySelector("[data-next]");
const scoreEl = document.querySelector("[data-score]");

function renderQuestion() {
  const item = structureIntroQuestions[quizState.index];
  const answer = quizState.answers.get(item.id);
  counterEl.textContent = `${quizState.index + 1} / ${structureIntroQuestions.length}`;
  metaEl.textContent = `${item.skill} ? ${item.subskill}`;
  questionEl.textContent = item.question;
  optionsEl.innerHTML = "";
  item.options.forEach((option) => {
    const button = document.createElement("button");
    button.className = "choice-button";
    button.type = "button";
    button.innerHTML = `<b>${option.key}</b><span>${option.text}</span>`;
    if (answer) {
      button.disabled = true;
      if (option.key === item.correctKey) button.classList.add("correct");
      if (option.key === answer.selectedKey && !answer.correct) button.classList.add("incorrect");
    }
    button.addEventListener("click", () => chooseAnswer(item, option));
    optionsEl.appendChild(button);
  });
  renderFeedback(item);
  prevBtn.disabled = quizState.index === 0;
  nextBtn.disabled = quizState.index === structureIntroQuestions.length - 1;
  updateScore();
}

function chooseAnswer(item, option) {
  const correct = option.key === item.correctKey;
  quizState.answers.set(item.id, { selectedKey: option.key, correct });
  renderQuestion();
}

function renderFeedback(item) {
  const answer = quizState.answers.get(item.id);
  if (!answer) {
    feedbackEl.hidden = true;
    feedbackEl.innerHTML = "";
    return;
  }
  feedbackEl.hidden = false;
  feedbackEl.className = `practice-feedback ${answer.correct ? "is-correct" : "is-incorrect"}`;
  feedbackEl.innerHTML = `
    <strong>${answer.correct ? "Correct" : "Incorrect"}</strong>
    <p>The correct answer is <b>${item.correctKey}. ${item.correctAnswer}</b></p>
    <p>${item.explanation}</p>
  `;
}

function updateScore() {
  const answered = [...quizState.answers.values()];
  const correct = answered.filter((answer) => answer.correct).length;
  scoreEl.textContent = `${correct}/${answered.length || 0} correct`;
}

prevBtn.addEventListener("click", () => {
  if (quizState.index > 0) {
    quizState.index -= 1;
    renderQuestion();
  }
});

nextBtn.addEventListener("click", () => {
  if (quizState.index < structureIntroQuestions.length - 1) {
    quizState.index += 1;
    renderQuestion();
  }
});

renderQuestion();

const readingState = { index: 0, answers: new Map() };

const passageTitleEl = document.querySelector("[data-reading-title]");
const passageTextEl = document.querySelector("[data-reading-text]");
const questionEl = document.querySelector("[data-reading-question]");
const metaEl = document.querySelector("[data-reading-meta]");
const optionsEl = document.querySelector("[data-reading-options]");
const feedbackEl = document.querySelector("[data-reading-feedback]");
const counterEl = document.querySelector("[data-reading-counter]");
const prevBtn = document.querySelector("[data-reading-prev]");
const nextBtn = document.querySelector("[data-reading-next]");
const scoreEl = document.querySelector("[data-reading-score]");

function paragraphs(text) {
  if (Array.isArray(text)) {
    return text.map((block) => `<p>${block.content || ""}</p>`).join("");
  }
  return text.split(/\n\s*\n/).map((p) => `<p>${p}</p>`).join("");
}

function renderReadingQuestion() {
  const item = readingIntroPassage.items[readingState.index];
  const answer = readingState.answers.get(item.id);
  passageTitleEl.textContent = readingIntroPassage.title;
  passageTextEl.innerHTML = paragraphs(readingIntroPassage.text);
  counterEl.textContent = `${readingState.index + 1} / ${readingIntroPassage.items.length}`;
  metaEl.textContent = `${item.questionType || item.skill} ? ${item.difficulty || "practice"}`;
  questionEl.textContent = item.question;
  optionsEl.innerHTML = "";
  item.options.forEach((option) => {
    const button = document.createElement("button");
    button.className = "choice-button";
    button.type = "button";
    button.innerHTML = `<b>${option.key}</b><span>${option.text}</span>`;
    if (answer) {
      button.disabled = true;
      if (option.key === item.correctKey) button.classList.add("correct");
      if (option.key === answer.selectedKey && !answer.correct) button.classList.add("incorrect");
    }
    button.addEventListener("click", () => chooseReadingAnswer(item, option));
    optionsEl.appendChild(button);
  });
  renderReadingFeedback(item);
  prevBtn.disabled = readingState.index === 0;
  nextBtn.disabled = readingState.index === readingIntroPassage.items.length - 1;
  updateReadingScore();
}

function chooseReadingAnswer(item, option) {
  const correct = option.key === item.correctKey;
  readingState.answers.set(item.id, { selectedKey: option.key, correct });
  renderReadingQuestion();
}

function renderReadingFeedback(item) {
  const answer = readingState.answers.get(item.id);
  if (!answer) {
    feedbackEl.hidden = true;
    feedbackEl.innerHTML = "";
    return;
  }
  feedbackEl.hidden = false;
  feedbackEl.className = `practice-feedback ${answer.correct ? "is-correct" : "is-incorrect"}`;
  feedbackEl.innerHTML = `
    <strong>${answer.correct ? "Correct" : "Incorrect"}</strong>
    <p>The correct answer is <b>${item.correctKey}. ${item.correctAnswer}</b></p>
    <p>${item.explanation}</p>
  `;
}

function updateReadingScore() {
  const answered = [...readingState.answers.values()];
  const correct = answered.filter((answer) => answer.correct).length;
  scoreEl.textContent = `${correct}/${answered.length || 0} correct`;
}

prevBtn.addEventListener("click", () => {
  if (readingState.index > 0) {
    readingState.index -= 1;
    renderReadingQuestion();
  }
});

nextBtn.addEventListener("click", () => {
  if (readingState.index < readingIntroPassage.items.length - 1) {
    readingState.index += 1;
    renderReadingQuestion();
  }
});

renderReadingQuestion();
